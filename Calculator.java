class Calculator{

public static void main(String[] args){
Calculator calc = new Calculator();
calc.add();
calc.sub();
calc.mul();
calc.div();
}
void add(){
System.out.println(10+20);
}
void sub(){
System.out.println(20-10);
}
void mul(){
System.out.println(10*20);
}
void div(){
System.out.println(20/10);
}
}
